package edu.gwu.algorithms.utils;

/** Some utility methods. */
public class Utils {

    public static int max(int arg1, int arg2, int arg3) {
        return Math.max(arg1, Math.max(arg2, arg3));
    }

    public static double max(double arg1, double arg2, double arg3) {
        return Math.max(arg1, Math.max(arg2, arg3));
    }

    public static float max(float arg1, float arg2, float arg3) {
        return Math.max(arg1, Math.max(arg2, arg3));
    }
}
